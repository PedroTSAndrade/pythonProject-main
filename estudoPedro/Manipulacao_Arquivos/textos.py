texto = "texto alternativo"
#        01234567890123456
#        65432109876543210 -
print(texto[0:5])   # texto
print(texto[6:])    # alternativo
print(texto[-11:])  # alternativo de trás para frente
print(texto[::-1])  # irá percorrer o texto inteiro de forma invertida
print(texto[0::])   # irá percorrer o texto inteiro na ordem correta